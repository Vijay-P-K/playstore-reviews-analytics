/* 4. What is the total amount of revenue generated by the google play store by hosting apps? 
(Whenever a user buys apps from the google play store,
the amount is considered in the revenue) */


SELECT SUM(PRICE) AS TOTAL_AMNT_REV FROM PLAY_STORE_APPS WHERE UPPER(`TYPE`) = 'PAID';

-- total amount of revenue genarated by google playstore by hosting apps is 10408.85


/* 5. Which Category of google play store apps has the highest number of installs? 
also, find out the total number of installs for that particular category. */


SELECT CATEGORY, SUM(INSTALLS) AS SUM_INST FROM PLAY_STORE_APPS GROUP BY 1 ORDER BY SUM_INST DESC LIMIT 10;

-- Game category has highest number of installs with total installation of 29932164264


-- 6. Which Genre has the most number of published apps?


