/* 11. Get the number of reviews having positive sentiment 
and number of reviews having negative sentiment for the app 10 best foods for you  
and compare them. */

SELECT
	SENTIMENT,
    COUNT(*) AS NUM_OF_SENTIMENTS
FROM
	PLAY_STORE_REVIEWS
WHERE 
	UPPER(APP) = '10 BEST FOODS FOR YOU' AND UPPER(SENTIMENT) IN ('POSITIVE', 'NEGATIVE')
GROUP BY 1;

-- There are 5 Negative sentiments and 79 Positive sentiments are present


/* 12. Which comments of ASUS SuperNote have sentiment polarity and sentiment subjectivity both as 1? */

SELECT * FROM PLAY_STORE_REVIEWS
WHERE UPPER(APP) = 'ASUS SUPERNOTE' 
AND SENTIMENT_POLARITY = 1
AND SENTIMENT_SUBJECTIVITY = 1;


